# Write a Python program to calculate the area of rectangle and square.

print("\n1. For rectangle: ")
length = float(input("Enter length of rectangle: "))
breadth = float(input("Enter breadth of rectangle: "))
print(f"The area of rectangle = {length * breadth}")
print("-" * 20)

print("\n2. For square: ")
side = float(input("Enter side of a square: "))
print(f"The area of the square = {side * side}")
print("-" * 20)
